#include <stdio.h>

int main() {
    int a, b, sum;

    // Taking input from user
    printf("Enter first number: ");
    scanf("%d", &a);

    printf("Enter second number: ");
    scanf("%d", &b);

    // Adding two numbers
    sum = a + b;

    // Display result
    printf("Sum = %d\n", sum);

    return 0;
}
